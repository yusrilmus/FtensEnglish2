  <h2>Ciri Simple Past Tense : </h2><br>
  <pre>
         <h3>1.> Selalu menggunkan Verb 2<br>
      Contoh : ete, talked, stopped, learnt, studied, etc.
                          </h3><br><br><br>

         <h3>2.> Biasanya menggunakan kata keterangan tertentu<br>
      Contoh : yesterday, last night, last week, two days ago, etc.</h3><br><br><br>

         <h3>3.> Yang perlu diperhatikan penggunaan verb, di Simple past ada dua macam verb yang dapat digunakan, yaitu :
      #>irreguler verb adalah kata kerja tidak beraturan
        EX :  drink = drank = drunk
              eat   = ate   = eaten
              go    = went  = gone

      #>reguler verb adalah kata kerja beraturan
        EX :  work    = worked   = worked
              clean   = cleaned  = cleaned
              compare = compared = compared

