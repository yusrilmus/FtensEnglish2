  <h2>Pengertian Parts Of Speech (Teori Dasar)</h2>
      Parts of speech atau disebut juga kinds of word adalah bagian-bagian yang digunakan untuk membentuk suatu kalimat dalam bahasa inggris.
      Kalian pernah pakai noun, verb, adjective, dan lainnya dalam berkomunikasi bahasa inggris? Nah, noun dan sejenisnys itulah yang dinamakan parts of speech.

      Macam-macam Parts Of Speech
      1. Verb (Kata Kerja)
          Ini adalah kata yang menyatakan suatu aktifitas atau kondisi. Berdasarkan fungsinya dalam kalimat, verb dibagi menjadi dua yaitu sebagai kata kerja bantu
          dan kata kerja utama. Kata kerja ada bantak sekali, beberapa contohnya adalah sebagai berikut: Be, Have, Do, Will, Can, Study, Speak, Love.
          Contoh dalam kalimat: They are studying english.

      2. Noun (Kata Benda)
          Kata benda adalah kata yang digunakan untuk menamai suatu benda baik abstrak maupun kongkrit. Abstrak itu benda yang tidak bisa dirasakan oleh indra manusia,
          contohnya adalah happiness, love, gas, air, dan sebagainya. Sedangkan kongkrit adalah kebalikannya contohnya adalah table, book, chair, boy, dan sebagainya.
          Contoh dalam kalimat: I buy a book.

      3. Pronoun (Kata Ganti)
          Pronoun adalah kata yang digunakan untuk menggantikan benda. Contohnya adalah I, you, we, they, it, he, she, her, him, them, me, us, your, their, our, my,
          dan masih banyak lagi.
          Contoh dalam kalimat: I love her.

      4. Adjective (Kata Sifat)
          Adjective adalah kata yang digunakan untuk menerangkan noun atau pronoun yang dapat berupa orang (person), tempat (place), binatang (animal), dan benda
          baik kongkrit maupun abstrak. Contohnya adalah beautiful, lazy, smart, sleepy, late, tired, delicious, tall, short, white, busy, red, dan sebagainya.
          Contoh dalam kalimat: I was so busy last night.

      5. Adverb (Kata Keterangan)
          Adverb adalah kata yang berfungsi menerangkan kata kerja (verb), kata sifat (adjective), atau kata keterangan lainnya. Contohnya adalah always, often, very,
          quickly, beautifully, completely, well, enough, really, now, today, yesterday, likely, maybe, probably, dan sebagainya.
          Contoh dalam kalimat: They often wear flat shoes.

      6. Conjuction (Kata Sambung)
          Conjunction adalah kata atau kelompok kata yang berfungsi menghubungkan antar kata, frasa, klausa, atau antar paragraf. Contohnya adalah and, but, or,
          so, while, when, where, after, before, because, although, dan sebagainya.
          Contoh dalam kalimat: Budi play music well, but he canâ€™t sing.

      7. Preposition (Kata Depan)
          Ada banyak sekali yang termasuk preposition, beberapa di antaranya adalah of, to, from, in, on, with, behind, because of, in spite of, despite,
          in front of, dan masih banyak lagi.
          Contoh dalam kalimat: She comes from America.

      8. Interjection (Kata Seru)
          Interjection adalah kata pendek yang diucapkan untuk mengekspresikan sesuatu seperti emosi, kagum, kecewa, dan sebagainya. Contoh dari interjection
          adalah wow!, ach!, pardon!, sorry!, excellent!, wonderful!, amazing!, good!, well!, oh my god!, dan sebagainya.
          Contoh dalam kalimat: Oh my god! Are you okay?
             
